package Belajar;

public class foreacha {
    public static void main(String[] args) {
        String nama [] = {"ziyad","kamilk","mamsda"};

        for (String  namas : nama) {
            System.out.println(namas);
            
        }
        for (int i = 0;  i< nama.length; i++){
            System.out.println(nama[i]);
        }
    }
    
}
